# --- –í–µ–±-—Å–µ—Ä–≤–µ—Ä –¥–ª—è Render ---
async def health_check(request):
    """Health check endpoint –¥–ª—è Render"""
    return web.Response(text="Cooking Bot is running OK")

async def db_health_check(request):
    """–ü—Ä–æ–≤–µ—Ä–∫–∞ –∑–¥–æ—Ä–æ–≤—å—è –ë–î"""
    try:
        stats = await db.get_stats()
        return web.json_response({
            "status": "healthy",
            "database": "connected",
            "stats": stats
        })
    except Exception as e:
        return web.json_response({
            "status": "unhealthy",
            "database": "disconnected",
            "error": str(e)
        }, status=500)

async def start_web_server():
    """–ó–∞–ø—É—Å–∫ –≤–µ–±-—Å–µ—Ä–≤–µ—Ä–∞ –¥–ª—è Render"""
    try:
        app = web.Application()
        app.router.add_get('/', health_check)
        app.router.add_get('/health', health_check)
        app.router.add_get('/db-health', db_health_check)
        
        runner = web.AppRunner(app)
        await runner.setup()
        
        port = int(os.environ.get("PORT", 8080))
        site = web.TCPSite(runner, '0.0.0.0', port)
        await site.start()
        
        logger.info(f"‚úÖ WEB SERVER STARTED ON PORT {port}")
        
        # –ó–∞–ø—É—Å–∫–∞–µ–º –ø–µ—Ä–∏–æ–¥–∏—á–µ—Å–∫–∏–µ –∑–∞–¥–∞—á–∏
        asyncio.create_task(periodic_cleanup())
        
    except Exception as e:
        logger.error(f"‚ùå Error starting web server: {e}")

